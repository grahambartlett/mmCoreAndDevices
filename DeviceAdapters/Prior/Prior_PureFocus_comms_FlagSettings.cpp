int PureFocus850AutoFocus::GetServoLimit(bool& isActive, double& maxPositive, double& maxNegative)
{
	int ret = DEVICE_OK;

	commsMutex.Lock();

	// First clear serial port from previous stuff
	ret = Prior::ClearPort(*this, *GetCoreCallback(), port);

	if (ret == DEVICE_OK)
	{
		// Send command
		std::ostringstream command;
		command << "SERVOLIMIT";

		ret = SendSerialCommand(port.c_str(), command.str().c_str(), "\r");

		if (ret == DEVICE_OK)
		{
			// Block/wait for acknowledge, or until we time out
			std::string answer;
			ret = GetSerialAnswer(port.c_str(), "\r", answer);

			if (ret == DEVICE_OK)
			{
				// Parse response
				if ((answer.length() > 2) && (answer[0] == 'E'))
				{
					int errNo = atoi(answer.substr(2).c_str());
					ret = ERR_OFFSET + errNo;
				}
				else
				{
					// Check and tokenise response
					size_t endA = 0;
					size_t endB = 0;

					endA = answer.find(',');
					if ((endA == std::string::npos) || (endA == 0) || (endA >= (answer.length() - 1)))
					{
						ret = ERR_UNRECOGNIZED_ANSWER;
					}
					else
					{
						endB = answer.find(',', endA + 1);
						if ((endB == std::string::npos) || ((endB - endA) < 2) || (endB >= (answer.length() - 1)))
						{
							ret = ERR_UNRECOGNIZED_ANSWER;
						}
					}

					if (ret == DEVICE_OK)
					{
						if (answer[0] == '0')
						{
							isActive = false;
						}
						else if (answer[0] == '1')
						{
							isActive = true;
						}
						else
						{
							ret = ERR_UNRECOGNIZED_ANSWER;
						}

						if (ret == DEVICE_OK)
						{
							// Limits are set in um, but values on the unit are set in 100nm steps
							maxPositive = atof(answer.substr(endA + 1, endB).c_str()) * 0.1;
							maxNegative = atof(answer.substr(endB + 1).c_str()) * 0.1;
						}
					}
				}
			}
		}
	}

	commsMutex.Unlock();

	return ret;
}
